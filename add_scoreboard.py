def lf_md5(data):
    return hashlib.md5(data.replace(b"\r\n", b"\n")).hexdigest()

def main():
    if not os.path.exists(APP):
        print("[중단] app.py 가 없습니다. 경마분석서버 폴더에서 실행하세요."); return 1
    raw = open(APP, "rb").read()
    if b"_scoreboard_daily" in raw:
        print("[완료] 이미 성적표 코드가 들어있습니다. 서버 재시작만 하면 됩니다."); return 0
    cur = lf_md5(raw)
    if cur != OLD_MD5:
        print("[중단] app.py 내용이 예상(bd6fa58)과 다릅니다: md5=" + cur)
